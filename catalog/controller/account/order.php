			if (isset($this->request->get['order_product_id'])) {
				$order_product_id = $this->request->get['order_product_id'];
			} else {
				$order_product_id = 0;
			}

			$order_product_info = $this->model_account_order->getOrderProduct($order_id, $order_product_id);


			if ($order_product_info) {
				$this->load->model('catalog/product');

				$product_info = $this->model_catalog_product->getProduct($order_product_info['product_id']);

				if ($product_info) {
					$option_data = array();

					$order_options = $this->model_account_order->getOrderOptions($order_product_info['order_id'], $order_product_id);

					foreach ($order_options as $order_option) {
						if ($order_option['type'] == 'select' || $order_option['type'] == 'radio' || $order_option['type'] == 'image') {
							$option_data[$order_option['product_option_id']] = $order_option['product_option_value_id'];
						} elseif ($order_option['type'] == 'checkbox') {
							$option_data[$order_option['product_option_id']][] = $order_option['product_option_value_id'];
						} elseif ($order_option['type'] == 'text' || $order_option['type'] == 'textarea' || $order_option['type'] == 'date' || $order_option['type'] == 'datetime' || $order_option['type'] == 'time') {
							$option_data[$order_option['product_option_id']] = $order_option['value'];
						} elseif ($order_option['type'] == 'file') {
							$option_data[$order_option['product_option_id']] = $this->encryption->encrypt($order_option['value']);
						}
					}
					$qtyArray = [];
					$qtyArray[$order_product_info['product_variant_id']] = $order_product_info['quantity'];

					if($product_info['is_bespoke'] == 1){
						$product_bespoke = $this->model_account_order->getOrderBespoke($order_product_id);

						$svg_bespoke_xml = json_decode($product_bespoke['svg_json'], true);
					//	print_r($svg_bespoke_xml);
						//now create a bound box to make it look less than shite!
						$svg_code = '<svg id="SvgjsSvg1295" width="100px" height="100px" xmlns="http://www.w3.org/2000/svg" version="1.1" xmlns:xlink="http://www.w3.org/1999/xlink" xmlns:svgjs="http://svgjs.com/svgjs" ';
						$bound_width = $svg_bespoke_xml['signWidth'] * 3.7795275591;
						$bound_height = $svg_bespoke_xml['signHeight'] * 3.7795275591;
						$svg_code .= 'viewBox="0 0 '.$bound_width. ' '. $bound_height. '">';
						$svg_code .= '<rect id="thumbSignFrame" width="'.$svg_bespoke_xml['signWidth'].'" height="'.$svg_bespoke_xml['signHeight'].'"'.' fill="none" stroke="black" stroke-width="1mm" x="0" y="0"></rect>';
						$svg_code .= $product_bespoke['svg_export'] .'</svg>';



						$this->cart->addBespoke($order_product_info['product_id'], $option_data, 0, $qtyArray, html_entity_decode($svg_code), html_entity_decode($product_bespoke['svg_json']), html_entity_decode($product_bespoke['svg_export']), null, html_entity_decode($product_bespoke['sign_text']) );
					//	$this->cart->addBespoke($this->request->post['product_id'], $option, $recurring_id, $varQtyArray,html_entity_decode($this->request->post['svg_raw']) ,html_entity_decode($this->request->post['svg_json']), html_entity_decode($this->request->post['svg_export']), html_entity_decode($this->request->post['svg_bespoke_images']), html_entity_decode($this->request->post['svg_bespoke_texts']));

					}
					else{
							$this->cart->add($order_product_info['product_id'], $option_data, 0, $qtyArray );
					}


					//add($product_id, $option = array(), $recurring_id = 0, $qtyArray = array())


					$this->session->data['success'] = sprintf($this->language->get('text_success'), $this->url->link('product/product', 'product_id=' . $product_info['product_id']), $product_info['name'], $this->url->link('checkout/cart'));

					unset($this->session->data['shipping_method']);
					unset($this->session->data['shipping_methods']);
					unset($this->session->data['payment_method']);
					unset($this->session->data['payment_methods']);
				} else {
					$this->session->data['error'] = sprintf($this->language->get('error_reorder'), $order_product_info['name']);
				}
			}
		}

		$this->response->redirect($this->url->link('account/order/info', 'order_id=' . $order_id));
	}
